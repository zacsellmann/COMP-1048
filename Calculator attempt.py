class Calculator:
    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        return a / b


calc = Calculator()
print(calc.add(2,41))

calc = Calculator()
print(calc.subtract(41,2))

calc = Calculator()
print(calc.multiply(41,2))

calc = Calculator()
print(calc.divide(41,2))

